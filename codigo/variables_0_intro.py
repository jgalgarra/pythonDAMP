# Variables numéricas enteras (sin decimales). En python se llaman del tipo int

a = 3                # Esta instrucción ordena cargar el número 3 en la variable a
b = 7                # Esta instrucción ordena cargar el número 7 en b
c = a + b            # Esta instrucción ordena cargar la suma de a y b en la variable c
print("El valor de a es ",a)
print("El valor de b es ",b)
print("La suma de a y b es ",c)

      
# Variables con valores reales (decimales). Se llaman float
peso = 76.5
altura = 179.5

print("Peso (Kg): ",peso)
print("Altura (cm): ",altura)

# En programación se llama string a una cadena de caracteres, y se indica escribiéndola entre
# comillas. En python son del tipo char

mi_nombre = "Javier"
print("Mi nombre es ",mi_nombre)
